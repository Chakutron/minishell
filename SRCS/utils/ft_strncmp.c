#include "../minishell.h"

int	ft_strncmp(char *s1, char const *s2, int n)
{
	int	i;

	i = 0;
	while (i < n)
	{
		if (s2[i] != s1[i])
			return (s1[i] - s2[i]);
		i++;
	}
	return (0);
}
